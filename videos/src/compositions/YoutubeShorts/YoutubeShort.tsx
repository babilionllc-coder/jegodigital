// ─── MAIN — 57 seconds @ 30fps = 1710 frames ───
// [0-90]   Hook card (3s)
// [90-750] Veo clip 1 (22s) + VO starts
// [750-1410] Veo clip 2 (22s) + VO continues
// [1410-1710] CTA card (10s)
export const YoutubeShort: React.FC<ShortProps> = ({
  hookStat, hookStatLabel, hookText, hookPillar,
  clip1, clip2, voiceover, captions, annotations = [], ctaLine1, educational,
}) => {
  const HOOK = 90;
  const CLIP = 660;
  const CTA = 300;

  return (
    <AbsoluteFill style={{ background: BG }}>
      <Sequence from={0} durationInFrames={HOOK}>
        <HookCard hookStat={hookStat} hookStatLabel={hookStatLabel} hookText={hookText} hookPillar={hookPillar} />
      </Sequence>

      <Sequence from={HOOK} durationInFrames={CLIP}>
        <VeoScene src={clip1} />
      </Sequence>

      <Sequence from={HOOK + CLIP} durationInFrames={CLIP}>
        <VeoScene src={clip2} />
      </Sequence>

      <Sequence from={HOOK + CLIP * 2} durationInFrames={CTA}>
        <CTACard ctaLine1={ctaLine1} educational={educational} />
      </Sequence>

      {/* Voiceover over both clips */}
      <Sequence from={HOOK} durationInFrames={CLIP * 2}>
        <Audio src={staticFile(voiceover)} volume={1} />
      </Sequence>

      {/* Captions — absolute frame timing already encoded in caption data */}
      <Sequence from={HOOK} durationInFrames={CLIP * 2}>
        <Captions captions={captions} />
      </Sequence>

      {/* Annotation overlays — narration-synced, auto-generated by annotation_system.py */}
      {annotations.length > 0 && (
        <Sequence from={HOOK} durationInFrames={CLIP * 2}>
          <AnnotationOverlay annotations={annotations} />
        </Sequence>
      )}
    </AbsoluteFill>
  );
};
