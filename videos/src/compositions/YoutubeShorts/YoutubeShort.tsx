import React from 'react';
import {
  useCurrentFrame,
  useVideoConfig,
  interpolate,
  spring,
  Audio,
  OffthreadVideo,
  staticFile,
  Sequence,
  AbsoluteFill,
} from 'remotion';

const GOLD = '#C5A059';
const BG = '#0f1115';
const CARD = '#1a1d24';
const WHITE = '#FFFFFF';
const MUTED = '#94a3b8';

export interface Caption {
  text: string;
  startSec: number;
  endSec: number;
}

export type AnnotationType = 'stat_burst' | 'icon_pop' | 'step_badge' | 'timer_pop' | 'warning_pop';

export interface Annotation {
  type: AnnotationType;
  startSec: number;
  endSec: number;
  // stat_burst: value = "21X"
  // icon_pop:   value = "📱", label = "WhatsApp", color = "#25D366"
  // step_badge: value = "1",  label = "Primer paso"
  // timer_pop:  value = "⏱️", label = "5 MIN"
  // warning_pop: value = "⏰ 12 HORAS", label = "Demasiado tarde"
  value: string;
  label?: string;
  color?: string;
}

export interface ShortProps {
  hookStat: string;
  hookStatLabel: string;
  hookText: string;
  hookPillar: string;
  clip1: string;
  clip2: string;
  voiceover: string;
  captions: Caption[];
  annotations?: Annotation[]; // narration-synced overlays — auto-generated by annotation_system.py
  ctaLine1?: string;
  educational?: boolean; // true = subscribe CTA, false = jegodigital.com CTA
}

